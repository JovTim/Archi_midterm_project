def splitter(value):
    binary = []
    number1 = []
    number2 = []

    for i in value:
        binary.append(i)

    for i in range(len(binary)):
        if binary[i] == '.':
            for j in binary[1 + i:]:
                number2.append(int(j))
            break
        number1.append(int(binary[i]))

    return number1, number2

def hexasplitter(value):
    hexadecimal = {'A': 10,
                   'B': 11,
                   'C': 12,
                   'D': 13,
                   'E': 14,
                   'F': 15}
    binary = []
    number1 = []
    number2 = []

    for i in value:
        binary.append(i)
    
    for i in range(len(binary)):
        if binary[i] == '.':
            for j in binary[1 + i:]:
                if j in hexadecimal:
                    number2.append(hexadecimal[j])
                else:
                    number2.append(int(j))
            break
        else:
            if binary[i] in hexadecimal:
                number1.append(hexadecimal[binary[i]])
            else:
                number1.append(int(binary[i]))
    
    return number1, number2

def decimalsplitter(value):
    x = ''
    y = ''
    
    for i in range(len(value)):
        if value[i] == '.':
            for j in value[i:]:
                y += j
            break
        x += value[i]
        
    if (x and y):
        return int(x), float(y)
    else:
        return int(x), 0

